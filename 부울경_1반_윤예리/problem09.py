# 입력을 받기위한 input 함수는 절대 사용하지 않습니다.
# 재귀함수를 이용하여 구현합니다.
def dec_to_bin(n):
    # 여기에 코드를 작성하여 함수를 완성합니다.

    # 나눌 수가 2보다 작을 때 함수가 종료됨
    if n == 0 or n == 1:
        # 마지막 나머지 추가
        return str(n)
    else:
        # 나머지를 반환하고 n을 2로 나눈 몫은 다시 함수에 넣음 (재귀)
        return dec_to_bin(n // 2) + str(n%2)

    

    



